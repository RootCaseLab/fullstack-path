'''
Дано натуральное число. Напишите программу, которая определяет, является ли последовательность его цифр при просмотре справа налево упорядоченной по неубыванию.

Формат входных данных
На вход программе подаётся натуральное число.

Формат выходных данных
Программа должна вывести «YES» (без кавычек), если последовательность его цифр при просмотре справа налево является упорядоченной по неубыванию, или «NO» (без кавычек) в противном случае.
'''

n = int(input())
flag = 'YES'
first_digit = 0

while n > 0:
    last_digit = n % 10

    if last_digit < first_digit:
        flag = ('NO')

    first_digit = last_digit
    n //= 10

print(flag)

